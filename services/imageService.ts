import { GoogleGenAI } from "@google/genai";

export async function generateLotusImage() {
  const ai = new GoogleGenAI({ apiKey: process.env.GEMINI_API_KEY! });
  const response = await ai.models.generateContent({
    model: 'gemini-2.5-flash-image',
    contents: {
      parts: [
        {
          text: 'A breathtakingly beautiful, single pink lotus flower in full bloom, ultra-detailed, ethereal glow, minimalist zen style, soft cinematic lighting, deep black background, high quality, buddhist aesthetic, spiritual and peaceful.',
        },
      ],
    },
    config: {
      imageConfig: {
        aspectRatio: "1:1",
      },
    },
  });

  for (const part of response.candidates[0].content.parts) {
    if (part.inlineData) {
      return `data:image/png;base64,${part.inlineData.data}`;
    }
  }
  return null;
}
